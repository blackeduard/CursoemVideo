#include <stdio.h>
#include <stdlib.h>

int main(){
  printf("\n");
  printf("Esse é um valor inteiro: %d\n", 99);
  printf("Esse é um valor real: %f\n", 99.887567567);
  printf("Esse é um valor real com 2 casas decimais: %.2f\n", 99.887567567);
  printf("Esse é um caractere: %c\n", 'A');
  printf("Essa é uma string: %s", "\"String é tipo o que está entre \"(texto)\".\"");
  return 0;
}