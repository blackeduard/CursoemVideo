pal=str(input('Digite algo: '))
print('\033[34mO tipo primitivo desse valor é {}\033[m'.format(type(pal)))
print('\033[34mSó tem espaços? {}\033[m'.format(pal.isspace()))
print('\033[34mÉ um número? {}\033[m'.format(pal.isnumeric()))
print('\033[34mÉ alfabético? {}\033[m'.format(pal.isalpha()))
print('\033[34mÉ alfanumérico? {}\033[m'.format(pal.isalnum()))
print('\033[34mEstá em maiúscula? {}\033[m'.format(pal.isupper()))
print('\033[34mEstá em minúscula? {}\033[m'.format(pal.islower()))
print('\033[34mEstá capitalizada? {}\033[m'.format(pal.istitle()))