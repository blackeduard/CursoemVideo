from time import sleep
sal=int(input('Digite o valor do seu salário: R$'))
print('\033[35mCALCULANDO O SEU SALÁRIO...\033[m')
sleep(3)
if sal >= 1250:
    print('O seu salário vai ganhar um aumento de \033[36m10%\033[m')
    print('\033[31m...\033[m')
    sleep(2)
    print('\033[31m...\033[m')
    sleep(2)
    print('\033[31mCalculando...\033[m')
    sleep(3)
    nvsal=sal+(sal*10/100)
    print('O seu salário era de \033[1;32mR${:.2f}\033[m ganhou um aumento de \033[36m10%\033[m, logo seu salário de agora é \033[1;32mR${:.2f}\033[m'.format(sal,nvsal))
else:
    print('O seu salário vai ganhar um aumento de \033[36m15%\033[m')
    print('\033[31m...\033[m')
    sleep(2)
    print('\033[31m...\033[m')
    sleep(2)
    print('\033[31mCalculando...\033[m')
    sleep(3)
    nvsal=sal+(sal*15/100)
    print('O seu salário era de \033[1;32mR${:.2f}\033[m ganhou um aumento de \033[36m15%\033[m, logo seu salário de agora é \033[1;32mR${:.2f}\033[m'.format(sal,nvsal))
print('\033[33mContinue trabalhando!\033[m')
